import mongoose from "mongoose";
const {Schema} = mongoose;


const chatSchema = new mongoose.Schema({
  senderId: {
    type: String,
    required: true,
  },
  receiverId: {
    type: String,
    required: true,
  },
  message: {
    type: String,
    required: true,
  },
  timestamp: {
    type: Date,
    default: Date.now,
  },
});

const chatModel= mongoose.model('chat' , chatSchema);
export default chatModel;
